#!/bin/bash

################################################################################
##
##    Env
##
################################################################################
hs_print Work Script

# scripts env
testnset -e -v HS_WORK_TOOLS -c "${HS_PATH_WORK}/tools"
testnset -e -v HS_WORK_SCRIPT -c "${HS_PATH_WORK}/scripts"

testnset -e -v HS_WORK_ZERO -c "zero.eth"

################################################################################
##
##    Alias
##
################################################################################

################################################################################
##
##    Tools
##
################################################################################

################################################################################
##
##    Customize Function
##
################################################################################

################################################################################
##
##    Hosts Settings
##
################################################################################

## Host specify script
case $(hostname -s) in
    "zero"|${HS_WORK_ZERO})
        if test -f "${HS_PATH_WORK}/hosts/zero.sh"
        then
            source ${HS_PATH_WORK}/hosts/zero.sh
        fi
        ;;
esac

################################################################################
## source hosts
################################################################################
if test -f "${HS_PATH_WORK}/hosts/$(hostname -s)"
then
    source "${HS_PATH_WORK}/$(hostname -s)"
fi

################################################################################
## source tools
################################################################################
if test -f "${HS_WORK_TOOLS}"
then
    epath "${HS_WORK_TOOLS}"
fi

################################################################################
## source scripts
################################################################################
if test -f "${HS_WORK_SCRIPT}"
then
    for each_scripts in ${HS_WORK_SCRIPT}/*.sh;
    do
        source ${each_scripts};
    done
fi
